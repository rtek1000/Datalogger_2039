		while (seconds_old == tm1.seconds) {
			IWDG_delay_ms(100);
		}

		if (tm1.seconds == 20) {
			lcd_bright_set = lcd_bright_mid;
		} else if (tm1.seconds == 40) {
			lcd_bright_set = lcd_bright_min;
		} else if (tm1.seconds == 0) {
			lcd_bright_set = lcd_bright_max;
		}
